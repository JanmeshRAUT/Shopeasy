export function getProductImage(productName) {
  // 1. Precise match
  if (productImages[productName]) {
    return productImages[productName];
  }

  // 2. Fuzzy match (case-insensitive and partial)
  const entry = Object.entries(productImages).find(([key]) => 
    productName.toLowerCase().includes(key.toLowerCase()) || 
    key.toLowerCase().includes(productName.toLowerCase())
  );
  if (entry) return entry[1];
  
  // 3. Category fallback
  const p = productName.toLowerCase();
  if (p.includes("headphone") || p.includes("earbud") || p.includes("audio")) 
    return "https://images.unsplash.com/photo-1505740420928-5e560c06d30e?w=600&q=80";
  if (p.includes("keyboard") || p.includes("mouse") || p.includes("input"))
    return "https://images.unsplash.com/photo-1587829191301-401b59b0b721?w=600&q=80";
  if (p.includes("cable") || p.includes("hub") || p.includes("usb"))
    return "https://images.unsplash.com/photo-1589227365533-dee630bb59bd?w=600&q=80";
  if (p.includes("laptop") || p.includes("monitor") || p.includes("pc"))
    return "https://images.unsplash.com/photo-1496181133206-80ce9b88a853?w=600&q=80";

  // 4. Ultimate fallback
  return "https://images.unsplash.com/photo-1519389950473-47ba0277781c?auto=format&fit=crop&w=600&q=80";
}
